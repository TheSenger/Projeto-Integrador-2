package Classes;

public class ClassePleb {
	
	private Float Sim;
	private Float Nao;
	private Float Nulo;
	private Float Total;
	
	
	public Float getSim() {
		return Sim;
	}
	public void setSim(Float sim) {
		Sim = sim;
	}
	public Float getNao() {
		return Nao;
	}
	public void setNao(Float nao) {
		Nao = nao;
	}
	public Float getNulo() {
		return Nulo;
	}
	public void setNulo(Float nulo) {
		Nulo = nulo;
	}
	public Float getTotal() {
		return Total;
	}
	public void setTotal(Float total) {
		Total = total;
	}
	

}
